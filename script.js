let currentLang = 'tr';

function setLanguage(lang) {
    currentLang = lang;
    document.getElementById('tr-btn').classList.remove('active');
    document.getElementById('en-btn').classList.remove('active');
    document.getElementById(lang + '-btn').classList.add('active');

    const elements = document.querySelectorAll('.lang');
    elements.forEach(el => {
        const key = el.getAttribute('key');
        if (translations[currentLang][key]) {
            if(el.children.length > 0 && el.tagName === 'A') {
                 const icon = el.firstElementChild.outerHTML;
                 el.innerHTML = icon + " " + translations[currentLang][key];
            } else {
                el.innerHTML = translations[currentLang][key];
            }
        }
    });
}

function toggleMenu() {
    const nav = document.getElementById('nav-list');
    nav.classList.toggle('active');
}

const accordions = document.querySelectorAll('.faq-question');
accordions.forEach(acc => {
    acc.addEventListener('click', function() {
        this.classList.toggle('active');
        const panel = this.nextElementSibling;
        if (panel.style.display === "block") {
            panel.style.display = "none";
            this.querySelector('i').classList.remove('fa-minus');
            this.querySelector('i').classList.add('fa-plus');
        } else {
            panel.style.display = "block";
            this.querySelector('i').classList.remove('fa-plus');
            this.querySelector('i').classList.add('fa-minus');
        }
    });
});